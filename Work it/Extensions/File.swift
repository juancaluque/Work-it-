import UIKit

extension UIButton {
    
    func setSelected() {
        self.backgroundColor = #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
    }
    
    func setDeselected() {
        self.backgroundColor = #colorLiteral(red: 0.5999459028, green: 0.6000347733, blue: 0.5999264121, alpha: 1)
    }
    
}
